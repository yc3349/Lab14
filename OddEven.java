public class OddEven{
    public static void oddEvenGenerator(int num)
    {   
        //if statement to determine whether the number is odd or even
         if (num % 2 == 0)
         {
             System.out.println(num + " is even");
        }
        else
        {
            System.out.println(num + " is odd");
        }
    }
    
 
}
